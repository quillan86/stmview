function [cmap] = cemult1(maplength);
% ColEdit function [cmap] = cemult1(maplength);
%
% colormap m-file written by ColEdit
% version 1.0 on 11-Mar-2000
%
% input  :	[maplength]	[64]	- colormap length
%
% output :	cmap			- colormap RGB-value array
 
% set red points
r = [ [];...
    [0 0.06383];...
    [0.16923 0.47872];...
    [0.25385 0.94681];...
    [0.375 0.95745];...
    [0.50513 0.5];...
    [0.625 0.021277];...
    [0.81795 0.52128];...
    [1 0.7766];...
    [] ];
 
% set green points
g = [ [];...
    [0 0.78723];...
    [0.125 0.87234];...
    [0.24359 0.17021];...
    [0.375 0.98936];...
    [0.49231 0.59574];...
    [0.625 0.010638];...
    [0.83077 0.51064];...
    [1 0.76596];...
    [] ];
 
% set blue points
b = [ [];...
    [0 0.97872];...
    [0.125 0.30851];...
    [0.25385 0.10638];...
    [0.375 0.095745];...
    [0.50256 0.44681];...
    [0.625 0.97872];...
    [0.81282 0.44681];...
    [1 0.78723];...
    [] ];
% ColEditInfoEnd
 
% get colormap length
if nargin==1 
  if length(maplength)==1
    if maplength<1
      maplength = 64;
    elseif maplength>256
      maplength = 256;
    elseif isinf(maplength)
      maplength = 64;
    elseif isnan(maplength)
      maplength = 64;
    end
  end
else
  maplength = 64;
end
 
% interpolate colormap
np = linspace(0,1,maplength);
rr = interp1(r(:,1),r(:,2),np,'linear');
gg = interp1(g(:,1),g(:,2),np,'linear');
bb = interp1(b(:,1),b(:,2),np,'linear');
 
% compose colormap
cmap = [rr(:),gg(:),bb(:)];
